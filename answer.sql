State and Explain the components of a DBMS(Database Management System)
DBMS is a software that facilitates the storage, mangement, retrival of data in an organized manner.
components: database schema which defines the organization of data.
            query processor which intreprates and executes database queries.
            database engine which allows the access and data management.
            data management language that allow the manipulation of data.
            security manager which controls users access to data.
            backup and recovery manager which protects data by providing regular updates and recovery mechanisms
            

